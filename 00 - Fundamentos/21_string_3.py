#TÉCNICA DE FATIAMENTO EM PYTHON

# exemplo = "Bianca Melquiades do Espirito Santo"
nome = "Bianca Melquiades do Espirito Santo"

# exemplo[] = índice ou número da localização
print(nome[0])
# exemplo[:9] = Start- Até onde vai
print(nome[:9])
# exemplo[10:] = End - Onde começa
print(nome[10:])
# exemplo[10:16] = Intervalo (start + end)
print(nome[10:16])
# exemplo[10:16:2] = Step - (start + end + número)
print(nome[10:16:2])
# exemplo[:] = Retorna tudo
print(nome[:])
# exemplo[::-1] = Inverte o que foi escrito
print(nome[::-1])