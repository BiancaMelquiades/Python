# "irmã da lista", é mais restritita, é imutável enquanto as lista é mutável

# ('laranja', 'pera', 'uva')
frutas = (
    "laranja",
    "pera",
    "uva",
)
print(frutas)

# criamos ela através da classe tuple
# ('p', 'y', 't', 'h', 'o', 'n')
letras = tuple("python")
print(letras)

# ou criamos coloando valores separados por VÍRGULA dentro de parenteses
# (1, 2, 3, 4)
numeros = tuple([1, 2, 3, 4])
print(numeros)

# a vírgula no final dentro do parentese sinaliza tupla
# ('Brasil',)
pais = ("Brasil",)
print(pais)
