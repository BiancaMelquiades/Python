# LISTA EM PYTHON
# Armazena de maneira sequencial

# forma mais comum, em [], com valores
frutas = ["laranja", "maca", "uva"]
print(frutas)

# lista vazia
frutas = []
print(frutas)

# construtor, neste exemplo, cada letra é um elemento (interável)
letras = list("python")
print(letras)

# a função range está gerando números para preencher os elementos na quantiodade desejada (10)
numeros = list(range(10))
print(numeros)

#exemplo carro
carro = ["Ferrari", "F8", 4200000, 2020, 2900, "São Paulo", True]
print(carro)
